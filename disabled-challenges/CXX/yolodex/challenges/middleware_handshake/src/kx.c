void cgc_kx_init_common(kx_t *kx)
{
    cgc_bn_init(&kx->P);
    cgc_bn_init(&kx->Q);
    cgc_bn_init(&kx->G);
    cgc_bn_init(&kx->a);
    cgc_bn_init(&kx->b);
    cgc_bn_init(&kx->S);
}

int cgc_kx_init_std(kx_t *kx, unsigned int group)
{
    if (group >= KX_GROUP_END)
        return FAILURE;

    cgc_kx_init_common(kx);

    if (cgc_bn_from_bytes(&kx->P, groups[group].P, sizeof(groups[group].P)) != SUCCESS)
        goto fail;
    if (cgc_bn_from_bytes(&kx->Q, groups[group].Q, sizeof(groups[group].Q)) != SUCCESS)
        goto fail;
    if (cgc_bn_from_bytes(&kx->G, groups[group].G, sizeof(groups[group].G)) != SUCCESS)
        goto fail;
    return SUCCESS;

fail:
    cgc_bn_destroy(&kx->P);
    cgc_bn_destroy(&kx->Q);
    cgc_bn_destroy(&kx->G);
    return FAILURE;
}

int cgc_kx_init(kx_t *kx, bn_t *P, bn_t *Q, bn_t *G)
{
    cgc_kx_init_common(kx);

    if (cgc_bn_copy(&kx->P, P) != SUCCESS)
        goto fail;
    if (cgc_bn_copy(&kx->Q, Q) != SUCCESS)
        goto fail;
    if (cgc_bn_copy(&kx->G, G) != SUCCESS)
        goto fail;
    return SUCCESS;

fail:
    cgc_bn_destroy(&kx->P);
    cgc_bn_destroy(&kx->Q);
    cgc_bn_destroy(&kx->G);
    return FAILURE;
}

void cgc_kx_destroy(kx_t *kx)
{
    cgc_bn_destroy(&kx->P);
    cgc_bn_destroy(&kx->Q);
    cgc_bn_destroy(&kx->G);
    cgc_bn_destroy(&kx->a);
    cgc_bn_destroy(&kx->b);
    cgc_bn_destroy(&kx->S);
}

int cgc_kx_gen_a(kx_t *kx, rng_t *rng)
{
    return cgc_bn_cgc_random(&kx->a, &kx->Q, rng);
}

int cgc_kx_get_A(kx_t *kx, bn_t *A)
{
    return cgc_bn_modexp(A, &kx->G, &kx->a, &kx->P);
}

int cgc_kx_set_b(kx_t *kx, bn_t *b)
{
    return cgc_bn_copy(&kx->b, b);
}

int cgc_kx_get_sk(kx_t *kx, unsigned char *out, unsigned int n)
{
    if (n >= cgc_bn_length(&kx->Q))
        return FAILURE;

    if (cgc_bn_modexp(&kx->S, &kx->b, &kx->a, &kx->P) != SUCCESS)
        return FAILURE;

    cgc_bn_to_bytes(&kx->S, out, n / 8);
    return SUCCESS;
}

